// Program using a while loop to display count down
import java.util.Scanner;
 
public class P07A9
{
   public static void main(String [] args)
   {
      
         // declare variables
      int counter=10;
      while (counter >=1)
         
      {   
      System.out.println (counter);
      counter--;
      }
      {
      System.out.println ("Blast Off!");
      
      }
         
                 
   } // end main method
}// end class 
