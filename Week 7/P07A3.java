// Program using a while loop to display digits 1-10 with separate line
import java.util.Scanner;
 
public class P07A3
{
   public static void main(String [] args)
   {
      
         // declare variables
      int counter=0;
      while (counter <=10)
         
      {   
      System.out.println (counter);
      counter ++;
      }
         
                 
   } // end main method
}// end class 
