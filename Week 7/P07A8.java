// Program using a while loop to display sentence 100 times with line number
import java.util.Scanner;
 
public class P07A8
{
   public static void main(String [] args)
   {
      
         // declare variables
      int counter=1;
      while (counter <=100)
         
      {   
      System.out.println (counter +" I must practise my programming skills. ");
      counter ++;
      }
         
                 
   } // end main method
}// end class 
