// Program using a while loop to display digits 1-10 with spaces
import java.util.Scanner;
 
public class P07A2
{
   public static void main(String [] args)
   {
      
         // declare variables
      int counter=1;
      while (counter <=10)
         
      {   
      System.out.print (counter +" ");
      counter ++;
      }
         
                 
   } // end main method
}// end class 
