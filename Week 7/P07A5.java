// Program using a while loop to display digits -9 to 5
import java.util.Scanner;
 
public class P07A5
{
   public static void main(String [] args)
   {
      
         // declare variables
      int counter=-9;
      while (counter <=5)
         
      {   
      System.out.println (counter);
      counter ++;
      }
         
                 
   } // end main method
}// end class 
